export interface PaperProps {
  /**
   * Shadow depth,
   * It accepts values between 0 and 24 inclusive.
   */
  elevation?: number;
  /**
   * if true edges will be rounded
   */
  squareBorder?: boolean;
  /**
   * Sets border,
   * It accepts values between 0 and 24 inclusive.
   */
  border?: number;
}
